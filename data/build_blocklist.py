def build_blocklist(parsed_entries):
    """Build the final blocklist combining explicit list + parsed note names."""
    # Collect all names from the note
    note_names = set(name for _, _, _, name, _ in parsed_entries)

    # Combine with hardcoded explicit list
    all_explicit = set(EXPLICIT_BLOCKLIST) | set(ADDITIONAL_FROM_NOTE) | note_names

    # Remove names that look like real Chinese people names (2-4 chars, all CJK)
    # unless they're in our known-junk list
    # Real people names: 2-4 CJK chars, no obvious junk patterns
    def looks_like_real_name(s):
        import unicodedata
        if len(s) < 2 or len(s) > 4:
            return False
        # Check all chars are CJK
        for c in s:
            if not (0x4E00 <= ord(c) <= 0x9FFF or 0x3400 <= ord(c) <= 0x4DBF):
                return False
        # Not in our junk categories
        return True

    # Names from note that look like real names (we still keep them - exact match is safe)
    # but we'll note them for awareness
    real_looking = [n for n in note_names if looks_like_real_name(n)
                    and n not in EXPLICIT_BLOCKLIST]

    print(f"\nNote: {len(real_looking)} note entries look like real Chinese names:")
    for n in sorted(real_looking):
        print(f"  {n}")
    print("These will be included in blocklist (exact match only)\n")

    # Deduplicate and sort
    explicit_list = sorted(all_explicit)

    # Pattern-based rules (regex)
    patterns = [
        r"^[\d]+$",                    # Pure numbers
        r"^.$",                        # Single character
        r"^[a-zA-Z]$",                 # Single Latin letter
        r"上一?页$",                    # 上页, 上一页
        r"下一?页$",                    # 下页, 下一页
        r".*跳转.*",                    # anything with 跳转
    ]

    # Title suffixes to strip (not delete - just strip from names)
    title_suffixes = [
        "研究员", "副研究员",
        "教授", "副教授",
        "讲师", "助理教授",
        "助教", "副",
    ]

    return {
        "explicit": explicit_list,
        "patterns": patterns,
        "title_suffixes": title_suffixes,
    }


def extract_affected_pairs(parsed_entries):
    """Get unique (school_code, school_name, department) tuples."""
    pairs = {}
    for code, school, prov, name, dept in parsed_entries:
        key = (code, school, dept)
        if key not in pairs:
            pairs[key] = {"school_code": code, "school_name": school,
                          "province": prov, "department": dept}
    return list(pairs.values())


