def parse_obsidian_note(path):
    """Parse all table rows from the markdown file, returning list of (code, school, prov, name, dept)."""
    entries = []
    with open(path, encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line.startswith("|"):
                continue
            # Split on pipe, strip whitespace
            cols = [c.strip() for c in line.split("|")]
            cols = [c for c in cols if c]  # remove empty from leading/trailing |
            if len(cols) < 4:
                continue
            # Skip header rows
            if cols[0] in ("院校代码", "-----", "---", "----- ") or all(
                c in ("", "-", "---", "-----", "----", "------- ", "---------- ")
                or set(c) == {"-"}
                for c in cols
            ):
                continue
            # Parse: code, school, province, name, dept
            try:
                code = cols[0].strip()
                school = cols[1].strip()
                prov = cols[2].strip() if len(cols) > 2 else ""
                name = cols[3].strip() if len(cols) > 3 else ""
                dept = cols[4].strip() if len(cols) > 4 else ""

                # Skip header-like rows
                if code == "院校代码" or name == "导师姓名" or name == "":
                    continue
                # Skip separator rows
                if all(c == "-" for c in code) or all(c == "-" for c in name):
                    continue

                if code and school and name:
                    entries.append((code, school, prov, name, dept))
            except Exception:
                continue
    return entries


